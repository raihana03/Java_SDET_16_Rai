package homework;

public class hw12Numbers {
    public static void main(String[] args) {

        int number=20;
       while (number>=1){
           System.out.print(number+" ");
           number-=2;
       }
        System.out.println();
       for (number=20; number>=1; number-=2){
           System.out.print(number+" ");
       }
    }
}
