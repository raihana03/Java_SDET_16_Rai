package class3;

public class E3Division {
    public static void main(String[] args) {

        System.out.println(10/5);
            //If the data type is int we will not see the decimal part
        System.out.println(11/5);
            //If the data type will be a double/float we still see the decimal part in answer
        System.out.println(11.3/5.0);
        int number1= 10;
        int number2= 4;
        System.out.println(number1/number2);

    }
}
