package class3;

public class E9ShortHandOperation {
    public static void main(String[] args) {
        //to create a container of type in and store the number 10 in it
        int number=10;
        //pick whatever is stored in number container and add 20 to that value and
        //store the results back to number variable
        number=number+20; //30
        System.out.println(number);
        number+=20;
        System.out.println(number);
    }
}
