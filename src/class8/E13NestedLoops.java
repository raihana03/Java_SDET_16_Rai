package class8;

public class E13NestedLoops {
    public static void main(String[] args) {
        /*
        2 2 2 2 2
        3 3 3 3 3
        4 4 4 4 4
         */

        for(int i=2; i<=4; i++){
            for (int j=1; j<=5; j++){
                System.out.print(i+" ");
            }
            System.out.println();
    }
}}
